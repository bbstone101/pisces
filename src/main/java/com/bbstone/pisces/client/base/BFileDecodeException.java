package com.bbstone.pisces.client.base;

public class BFileDecodeException extends Throwable {
    public BFileDecodeException() {
        super();
    }

    public BFileDecodeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public BFileDecodeException(String message, Throwable cause) {
        super(message, cause);
    }

    public BFileDecodeException(String message) {
        super(message);
    }

    public BFileDecodeException(Throwable cause) {
        super(cause);
    }

}
